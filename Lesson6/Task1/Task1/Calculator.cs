namespace Task1
{
    public static class Calculator
    {
        public static int Add(int one, int two)
        {
            return one+two;
        }

        public static int Substract(int one, int two)
        {
            return one - two;
        }

        public static int Multiply(int one, int two)
        {
            return one*two;
        }

        public static int Split(int one, int two)
        {
            return one/two;
        }
    }
}